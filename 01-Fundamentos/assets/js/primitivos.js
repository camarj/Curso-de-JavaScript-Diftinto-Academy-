// Tipos primitivos

// String
const nombre = 'Luiz';
console.log(typeof nombre);

// Number
const edad = 30;
console.log(typeof edad);

// Boolean
const isRegister = true;
console.log(typeof isRegister);

// Simbols  (ES6)
const simbolo1 = Symbol();
const simbolo2 = Symbol();
console.log(typeof simbolo1);

console.log(simbolo1 === simbolo2);

// Undefined
let nombre2;
console.log(typeof nombre2);

// Null
let nombre3 = null;
console.log(typeof nombre3);
